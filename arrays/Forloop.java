public class Forloop {
    public static void main(String []args){
        for(int j=1;j<=5;j++){
            for(int i=1;i<=5;i++){
                if(i==3)
                    break;
                System.out.println("J:" +j+" I=" +i);
            }
            
        }
    }
    
}
